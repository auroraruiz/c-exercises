#include <stdio.h>

int main(){
    printf("27. For loops");
    printf("\n");
    printf("-------------\n");
    // for loop = repeats a section of code a limited amount of times
    for (int i = 1; i <= 10; i++){
        printf("Counting: %i\n", i);
    }

    printf("--- Done ---");
    return 0;
}